public class Operations {
    public int sum(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int subtraction(int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int multiplication(int sayi1, int sayi2) {
        return sayi1 * sayi2;
    }

    public int division(int sayi1, int sayi2) {
        return sayi1 / sayi2;
    }
}
